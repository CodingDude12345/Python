# Explicit
name="Jackson"
age=0
gpa=1.9
student=True

type(name)
type(age)
type(gpa)
type(student)

print(type(name))
print(type(age))
print(type(gpa))
print(type(student))

age=float(age)
print(age)
print(type(age))

gpa=int(gpa)
print(gpa)

student=str(student)
print(student)
print(type(student))

age=bool(age)
print(age)
print(type(age))

name=bool(name)
print(name)
print(type(name))


